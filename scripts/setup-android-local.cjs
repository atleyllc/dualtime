#!/usr/bin/env node
/**
 * Writes android/local.properties with sdk.dir for local Gradle builds.
 * Run after `npm run prebuild:android` if the build says "SDK location not found".
 */

const fs = require('fs');
const os = require('os');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const androidDir = path.join(projectRoot, 'android');
const localProps = path.join(androidDir, 'local.properties');

function detectSdk() {
  const fromEnv = process.env.ANDROID_HOME || process.env.ANDROID_SDK_ROOT;
  if (fromEnv && fs.existsSync(fromEnv)) return fromEnv;

  const candidates = [
    path.join(os.homedir(), 'Library', 'Android', 'sdk'),
    path.join(os.homedir(), 'Android', 'Sdk'),
  ];

  for (const c of candidates) {
    if (fs.existsSync(c)) return c;
  }
  return null;
}

function main() {
  if (!fs.existsSync(androidDir)) {
    console.error('android/ not found. Run: npm run prebuild:android');
    process.exit(1);
  }

  const sdk = detectSdk();
  if (!sdk) {
    console.error(`
Could not find the Android SDK.

Install Android Studio, open SDK Manager, then:

  export ANDROID_HOME="$HOME/Library/Android/sdk"
  export PATH="$ANDROID_HOME/platform-tools:$PATH"
`);
    process.exit(1);
  }

  const sdkDir = sdk.replace(/\\/g, '/');
  fs.writeFileSync(
    localProps,
    `## Generated by scripts/setup-android-local.cjs — do not commit\nsdk.dir=${sdkDir}\n`,
    'utf8'
  );
  console.log(`Wrote ${path.relative(projectRoot, localProps)}`);
  console.log(`sdk.dir=${sdkDir}`);

  require('./patch-android-gradle.cjs');
  try {
    require('./install-android-sdk.cjs');
  } catch {
    /* optional in local dev */
  }
}

main();
